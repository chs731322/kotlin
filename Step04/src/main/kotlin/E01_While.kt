fun main() {

    /*

        While문
            - 조건이 참인 동안 반복
            - 조건이 거짓이면 반복문을 빠져나옴

        while(조건식) {
            //실행문
        }

        1. 제어변수 초기값
        2. 조건식
        3. 제어변수 증감, 변경

     */

    // 1~10까지 출력
    var i = 1

    while ( i <= 10 ) {
        print("$i ")
        i++
    }

}